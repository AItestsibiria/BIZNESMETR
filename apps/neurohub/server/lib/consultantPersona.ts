// По дефолту — взрослая женская персона. Мужские/teen/kid включаются
// только при явном переключении через бот. Хеш-стабильно.
export function personaFor(userKey: string): Persona {
  const pool = PERSONAS.filter(p => p.gender === "ж" && p.ageTier === "adult");
  let h = 0;
  const s = String(userKey || "anon");
  for (let i = 0; i < s.length; i++) h = ((h << 5) - h + s.charCodeAt(i)) | 0;
  return pool[Math.abs(h) % pool.length];
}

// Найти персону того же психотипа в другом ageTier (для смены на
// сверстника когда юзер ребёнок/подросток).
export function peerAgePersona(currentName: string, targetTier: AgeTier, preferGender?: "ж" | "м"): Persona | null {
  const current = PERSONAS.find(p => p.name === currentName);
  const psy = current?.psychotype || "warm";
  const gender = preferGender || current?.gender || "ж";
  return (
    PERSONAS.find(p => p.ageTier === targetTier && p.psychotype === psy && p.gender === gender) ||
    PERSONAS.find(p => p.ageTier === targetTier && p.gender === gender) ||
    PERSONAS.find(p => p.ageTier === targetTier) ||
    null
  );
}

// Найти персону противоположного пола с ТЕМ ЖЕ психотипом
// (Eugene 2026-05-12: сохраняем характер при смене пола).
// Если просили парня — отдаём мужчину той же психологической группы.
export function oppositeGenderPersona(currentName: string): Persona | null {
  const current = PERSONAS.find(p => p.name === currentName);
  if (!current) return null;
  const opposite = current.gender === "ж" ? "м" : "ж";
  return PERSONAS.find(p => p.gender === opposite && p.psychotype === current.psychotype) || null;
}

